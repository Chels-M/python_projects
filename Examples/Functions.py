# A function that returns a numbers square

# Takes the argument as an integer - can use any variable name there instead of 'num' but best to keep it relevant and
# readable
def square(num):
    return num * num


# 'num' could literally be the word 'Twat' and it would still work.

# This saves the function result into a variable called results , we have passed the argument in of 5 to be squared
# by the function, and it will pring the result to the console.
results = square(5)
print(results)
