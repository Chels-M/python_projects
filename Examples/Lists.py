# This program shows an example of a list in Python

numbers = [1, 2, 3, 4, 5]

print(numbers[0]) # prints 1
print(numbers[1]) # prints 2

# This assignes a new value to the index number 1 (which currently holds the value '2') replacing the number 2, with 10
numbers[1] = 10
print(numbers) # Prints [1, 10, 3, 4, 5]